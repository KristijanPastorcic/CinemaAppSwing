    @Override
    public void Login(Optional<User> user) {
        if (user.isPresent()) {
            this.user = user.get();
            this.setVisible(true);
            init();
        }
    }

    private void init() {
        repository = RepositoryFactory.getUsersRepository();
        roleManager = (RoleManager) repository;
        configurePanels();
    }


    // Variables declaration - do not modify//GEN-BEGIN:variables
